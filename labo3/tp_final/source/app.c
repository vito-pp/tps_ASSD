void App_Init (void)
{
    // leds init
    gpioMode(PIN_LED_BLUE, OUTPUT);
    gpioWrite(PIN_LED_BLUE, !LED_ACTIVE);
    gpioMode(PIN_LED_RED, OUTPUT);
    gpioWrite(PIN_LED_RED, !LED_ACTIVE);

    DMA_Init();
    PIT_Init();
    ADC_Init(true); // dma request = true
    DAC_Init();
}

